CREATE TABLE t_shp_order
(
   ID                   BIGINT NOT NULL AUTO_INCREMENT COMMENT '订单 ID',
   SHOP_ID              BIGINT NOT NULL COMMENT '门店 ID',
   ORDER_SOURCE_CODE    VARCHAR(32) NOT NULL COMMENT '订单 来源 代码',
   ORDER_NUM            VARCHAR(32) NOT NULL COMMENT '订单 编号',
   GOODS_TOTAL_AMOUNT   DOUBLE NOT NULL COMMENT '商品 总 金额',
   COST_TOTAL_AMOUNT    DOUBLE NOT NULL COMMENT '成本 总 金额',
   ORDER_TOTAL_AMOUNT   DOUBLE NOT NULL COMMENT '订单 总 金额',
   GROSS_MARGIN         DOUBLE NOT NULL COMMENT '毛利',
   OPEN_ORDER_MAN_ID    BIGINT COMMENT '开单 人 ID',
   MEMBER_CARD_NUM      VARCHAR(32) NOT NULL COMMENT '会员 卡 号码',
   MEMBER_ID            BIGINT NOT NULL COMMENT '会员 ID',
   REMARK               VARCHAR(255) COMMENT '备注',
   FREIGHT_AMOUNT       DOUBLE NOT NULL COMMENT '运费',
   PAYMENT_WAY_CODE     VARCHAR(32) NOT NULL COMMENT '支付 方式 代码',
   DELIVERY_WAY         VARCHAR(32) COMMENT '配送 方式',
   ORDER_STAT_CODE      VARCHAR(32) NOT NULL COMMENT '订单 状态 代码',
   IS_PAYED             VARCHAR(32) NOT NULL COMMENT '是否 已支付',
   IS_COD               VARCHAR(32) NOT NULL COMMENT '是否 货到 付款',
   ORDER_CONFIRM_TIME   TIMESTAMP COMMENT '订单 确认 时间',
   FINISH_TIME          TIMESTAMP COMMENT '订单 完成 时间',
   MEDICAL_INSURANCE_PAYMENT_AMOUNT DOUBLE NOT NULL COMMENT '医保 支付 金额',
   REAL_GEVE_CASH_AMOUNT DOUBLE NOT NULL COMMENT '实收 现金 金额',
   RETURN_SMALL         DOUBLE NOT NULL COMMENT '找零',
   SEND_OUT_TIME        TIMESTAMP COMMENT '发货 时间',
   DELIVERY_TIME        TIMESTAMP COMMENT '送达 时间',
   CLOSE_REASON_CODE    VARCHAR(32) COMMENT '关闭 原因 代码',
   ORDER_CLOSE_TIME     TIMESTAMP COMMENT '订单 关闭 时间',
   RECEIVER_NAME        VARCHAR(32) COMMENT '收货人 姓名',
   CONTACT_TEL          VARCHAR(32) COMMENT '联系 电话',
   DELIVERY_ADDR        VARCHAR(64) COMMENT '配送 地址',
   DETAIL_ADDR          VARCHAR(128) COMMENT '详细 地址',
   CREATE_DATE          TIMESTAMP NOT NULL COMMENT '创建时间',
   CREATE_BY            VARCHAR(32) NOT NULL COMMENT '创建人',
   LAST_MODIFIED_DATE   TIMESTAMP COMMENT '更新时间',
   LAST_MODIFIED_BY     VARCHAR(32) COMMENT '更新用户',
   VERSION              BIGINT NOT NULL COMMENT '版本',
   PRIMARY KEY (ID)
);
